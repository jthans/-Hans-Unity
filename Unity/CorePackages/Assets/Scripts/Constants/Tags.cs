namespace Hans.Unity.Tags 
{
    /// <summary>
    ///  Constants that represent the tags used in the Hans Core Packages .unitypackage, for easy access and
    ///     future reference, if others take this for development.
    /// </summary>
    public class Tags
    {
        /// <summary>
        ///  Enemy - Used for any health-tracked entity that opposes the player.
        /// </summary>
        public const string Enemy = "Enemy";

        /// <summary>
        ///  Item - Used for any inventory item that can be interacted with in the world.
        /// </summary>
        public const string InventoryItem = "Item";

        /// <summary>
        ///  Represents a player controller in the scene.
        /// </summary>
        public const string Player = "Player";
    }
}